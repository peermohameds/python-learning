# Print two statements
print("The itsy bitsy spider climbed up the waterspout.")
print("Down came the rain and washed the spider out.")

# Print two statements with one line of space
print("The itsy bitsy spider climbed up the waterspout.")
print()
print("Down came the rain and washed the spider out.")

# print multi line using \n
print("The itsy bitsy spider\nclimbed up the waterspout.")
print("Down came the rain \nand washed the spider out.")

# Print with escape sequences Double quotes
print("Doctor  : \"How are you doing?\"")
print("Patient : \"I am doing good\"")

# Print with escape sequences single quotes
print("Doctor  : \'How are you doing?\'")
print("Patient : \'I am doing good\'")

# print with multiple arguments
print("Hello!","Welcome to","Python","Programming.","This","programming","is","amazing.")

# print accepts 2 keyword arguments
# first is end. default value for end is "\n"
print("Hello","World", end="!!! \n")
# second is sep which will seperate string arguments
print("Hello","World", sep="-")

# mixed both end and sep keyword arguments
print("My", "name", "is", sep="-", end="-")
print("Peer", "Mohamed.", sep="*", end="*\n")

